    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //获取预设的闹钟时间
        int h = Integer.parseInt(String.valueOf(jComboBox1.getSelectedItem()));
        int m = Integer.parseInt(String.valueOf(jComboBox2.getSelectedItem()));
        
        //最多只能添加5个闹钟
        if(alarmNum<5){
            AlarmClock ac1 = new AlarmClock(h,m,clock1);
            //判断哪里有空位可以添加闹钟
            for(int i=0;i<5;i++){
                if(alarmEmpty[i]){
                    ac1.num = i;
                    alarmEmpty[i] = false;
                    break;
                }
            }
            jPanel2.add(ac1);
            //每一个新闹钟的位置较上一个下移30
            ac1.setBounds(5, 25+30*ac1.num, 300,30);
            alarmNum++;
            
            //监听AlarmClock组件中的remove按钮
            RemoveAction removeAction = new RemoveAction(ac1);
            ac1.remove.addActionListener(removeAction);
        }else{
            //多于5个闹钟则弹出警告窗口
            String mes = "If you want to add new alarm clock, please remove an old one.";
            JOptionPane.showMessageDialog(null,mes , "Too much alarm clocks!", JOptionPane.WARNING_MESSAGE, null);
        }
        
        jPanel2.repaint();
        
//        System.out.println("1");
    }//GEN-LAST:event_jButton1ActionPerformed

