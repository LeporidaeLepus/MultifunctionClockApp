    //jButton1ActionPerformed中remove按钮的监听事件
    public class RemoveAction implements ActionListener{
        AlarmClock ac;
        
        public RemoveAction(AlarmClock ac){
            this.ac = ac;
        }
        @Override
        public void actionPerformed(ActionEvent evt){
            //获取该闹钟位置序号
            int num = ac.num;
            
            jPanel2.remove(ac);
            //将代表原位置的布尔值重置为true，表示有空位
            alarmEmpty[num] = true;
            //闹钟数减1
            alarmNum--;
            jPanel2.repaint();
        }
    }
    
    //jButton2ActionPerformed中delete按钮的监听事件
    public class DeleteAction implements ActionListener{
        Countdown cd;
        
        public DeleteAction(Countdown cd){
            this.cd = cd;
        }
        
        @Override
        public void actionPerformed(ActionEvent evt){
            jPanel3.remove(cd);
            //将代表原位置的布尔值重置为true，表示有空位
            cdEmpty[cd.num] = true;
            //倒计时数减1
            cdNum--;
            
            jPanel3.repaint();
        }
    }
    
    
