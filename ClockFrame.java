    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //获取预设的倒计时时间
        int h = Integer.parseInt((String)jComboBox3.getSelectedItem());
        int m = Integer.parseInt((String)jComboBox4.getSelectedItem());
        int s = Integer.parseInt((String)jComboBox5.getSelectedItem());
        
        if(cdNum<3){
            Countdown cd = new Countdown(h,m,s);
            
            //最多添加三个倒计时
            for(int i=0;i<3;i++){
                if(cdEmpty[i]){
                    cd.num = i;
                    cdEmpty[i] = false;
                    break;
                }    
            }
            jPanel3.add(cd);
            //每一个新倒计时的位置较上一个下移30
            cd.setBounds(10, 25+30*cd.num, 300, 30);
            cdNum++;
            
            //监听Countdown组件中的delete按钮
            DeleteAction deleteAction = new DeleteAction(cd);
            cd.delete.addActionListener(deleteAction);
        }else{
            //多于3个倒计时则弹出警告窗口
            String mes = "If you want to add new countdown, please remove an old one.";
            JOptionPane.showMessageDialog(null,mes , "Too much countdowns!", JOptionPane.WARNING_MESSAGE, null);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

